void handleToggleAutoScroll() {
  autoScrollEnabled = !autoScrollEnabled;
  preferences.putBool("autoScroll", autoScrollEnabled);
  
  // Flash LED to indicate change
  flashLED(255, 255, 0, 300);
  
  server.send(200, "text/html", "<html><body><h1>Auto-Scroll " + String(autoScrollEnabled ? "Enabled" : "Disabled") + "!</h1><a href='/'>Back</a></body></html>");
}

void handleSetInterval() {
  if (server.hasArg("seconds")) {
    int seconds = server.arg("seconds").toInt();
    if (seconds >= 1 && seconds <= 60) {
      autoScrollInterval = seconds * 1000; // Convert to milliseconds
      preferences.putULong("scrollInterval", autoScrollInterval);
      
      // Flash LED to confirm
      flashLED(0, 255, 255, 300);
    }
  }
  server.send(200, "text/html", "<html><body><h1>Interval Set to " + String(autoScrollInterval / 1000) + " seconds!</h1><a href='/'>Back</a></body></html>");
}
