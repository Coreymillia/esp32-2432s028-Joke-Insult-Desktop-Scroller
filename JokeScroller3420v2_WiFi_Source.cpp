void displayCurrentJoke() {
    gfx->fillScreen(BLACK);
    
    // Title
    gfx->setTextSize(2);
    gfx->setTextColor(CYAN);
    gfx->setCursor(10, 10);
    gfx->print("JOKE SCROLLER");
    
    // Joke counter
    gfx->setTextSize(1);
    gfx->setTextColor(WHITE);
    gfx->setCursor(10, 40);
    gfx->printf("Joke %d of %d", currentJokeIndex + 1, numJokes);
    
    // Auto-scroll indicator
    if (autoScrollEnabled) {
        gfx->setTextColor(YELLOW);
        gfx->setCursor(200, 40);
        gfx->print("RANDOM");
    }
    
    // Display joke with word wrapping
    String joke = String(jokes[currentJokeIndex]);
    gfx->setTextSize(2);
    gfx->setTextColor(WHITE);
    
    int maxCharsPerLine = 26; // Approx for size 2 text
    int yPos = 70;
    int startPos = 0;
    
    while (startPos < joke.length()) {
        int endPos = startPos + maxCharsPerLine;
        
        if (endPos < joke.length()) {
            // Find last space before line limit
            int spacePos = joke.lastIndexOf(' ', endPos);
            if (spacePos > startPos) {
                endPos = spacePos;
            }
        } else {
            endPos = joke.length();
        }
        
        String line = joke.substring(startPos, endPos);
        gfx->setCursor(10, yPos);
        gfx->print(line);
        
        startPos = endPos;
        if (startPos < joke.length() && joke.charAt(startPos) == ' ') {
            startPos++; // Skip space
        }
        
        yPos += 25; // Line spacing
        
        if (yPos > 180) break; // Don't overflow screen
    }
    
    // Controls help
    gfx->setTextSize(1);
    gfx->setTextColor(GREEN);
    gfx->setCursor(10, 210);
    gfx->print("Touch: Next | Hold Boot: Random Auto-Scroll");
}

void setLED(uint8_t r, uint8_t g, uint8_t b) {
    // LEDs are active low - invert values
    analogWrite(LED_RED_PIN, 255 - r);
    analogWrite(LED_GREEN_PIN, 255 - g);
    analogWrite(LED_BLUE_PIN, 255 - b);
}

void flashLED(uint8_t r, uint8_t g, uint8_t b, int duration) {
    setLED(r, g, b);
    delay(duration);
    setLED(0, 0, 0); // Turn off
}

